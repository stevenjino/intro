//Immutable Variable
fun main(args: Array<String>) {
    val name="Steve"
    val name2="Mwaura"

//    name="Stephen" cannot be reassigned.
    println("My first name is " +name)
    println("My second name is " +name2)
    println("My full name is "+name +" " +name2)

//Mutable Variable
    var school="Emobilis"
    var school2="Mobile Technology Institute"
    school2="Academy"
    println("I studied at "+school +" "+school2)
}